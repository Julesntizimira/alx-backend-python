#!/usr/bin/env python3
'''define function concat()
'''


def concat(str1: str, str2: str) -> str:
    """
    concatenate two strings.

    Parameters:
    - str1 (str): The first string.
    - str2 (str): The second string.

    Returns:
    float: The concatenated string.
    """
    return str1 + str2
